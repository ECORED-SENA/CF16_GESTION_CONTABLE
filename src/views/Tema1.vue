      h2 1.5 Diferencia entre costo del producto y costo del periodo
    p El costo del producto o servicio es el que se da al realizar la adquisición de los tres elementos del costo: materias primas, mano de obra y costos indirectos de fabricación, para poder producir. Mientras que los costos del periodo son los gastos en los que se incurre en un periodo de tiempo determinado, los cuales se espera que a futuro no rindan beneficios. Se encuentran clasificados en gastos administrativos, gastos de ventas y gastos financieros. Se pueden tomar como ejemplo los costos del periodo, costos de venta, costos de mercadeo, costos de investigación y desarrollo, entre otros.

