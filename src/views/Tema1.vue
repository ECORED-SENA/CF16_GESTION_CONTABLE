      h2 1.6 Diferencia entre contabilidad de costos y contabilidad financiera
    p La contabilidad de costos, también conocida como contabilidad analítica, es una técnica contable que tiene como finalidad crear un sistema de información que permita conocer cuál es el costo de los productos fabricados. Es un instrumento que sirve de apoyo a la contabilidad financiera, estudiando la estructura de costes en las empresas. La contabilidad de costes consiste en realizar una imputación razonable de costes directos e indirectos que permita obtener información analítica en la que apoyarse para la toma de decisiones por parte de la dirección de la empresa (Sinisterra, 2017, p. 2). 
